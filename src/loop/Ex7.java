package loop;

public class Ex7 {

	public static void main(String[] args) {
		
		int sum = 0;
		// for(초기화, 조건식, 증감식)
		for (int i = 0; i <= 10; i++) {
			sum = sum + i;
		System.out.println("1부터 10까지 합은 " + sum + "입니다");	
			
		}
		
	}

}
 // for문과 while문은 똑같이 초기화, 조건식, 증감식을
 // 사용하지만 가독성이 좋은 것은 for문입니다.

